import math
print("sqrt:- ", math.sqrt(25))

print("Floor of (2.5):- ", math.floor(2.5)) #floor --> like home floor --> below or previous int value
print("Ceil of (2.5):- ", math.ceil(2.5)) #ceil --> like home ceiling --. above or next int value

print("square of 3:- ", 3**2) #square of 3
print("square of 3:- ", math.pow(3, 2)) #(number, power)

print("PI value:- ", math.pi) #constant value
print("epsilon (e) value :- ", math.e) #constant value

